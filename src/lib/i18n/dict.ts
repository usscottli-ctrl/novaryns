const DICTS: Record<Locale, Dict> = { zh, en };

function getRaw(obj: Dict, path: string): unknown {
  let cur: unknown = obj;
  for (const k of path.split(".")) {
    if (cur && typeof cur === "object" && k in (cur as Record<string, unknown>)) {
      cur = (cur as Record<string, unknown>)[k];
    } else {
      return undefined;
    }
  }
  return cur;
}

/** Raw lookup at a path for a locale (no fallback). */
export function lookup(locale: Locale, key: string): unknown {
  return getRaw(DICTS[locale], key);
}

/** Resolve a dot-path key to a string for a locale, falling back to zh then key.
 * 自动把字典里所有 "{brand}" 占位符替换成 NEXT_PUBLIC_BRAND_NAME (海外={brand},
 * 国内=星泽商图)。这样改品牌名只需要改一处 env,不用动每一个翻译键。 */
function applyBrand(s: string): string {
  return s.includes("{brand}") ? s.replaceAll("{brand}", BRAND) : s;
}
export function translate(locale: Locale, key: string): string {
  const v = getRaw(DICTS[locale], key);
  if (typeof v === "string") return applyBrand(v);
  const z = getRaw(DICTS.zh, key);
  if (typeof z === "string") return applyBrand(z);
  return key;
}
